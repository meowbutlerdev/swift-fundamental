/**
 메모리와 CPU 개념 요약
 - CPU: 컴퓨터의 중앙처리장치로, 프로그램의 연산을 실행하고 데이터를 직접 처리하는 역할을 함.
 - 메모리(RAM): 프로그램이 실행될 때 데이터를 저장하는 임시 공간.
 - 예: 채팅앱의 친구 목록이나 메시지가 메모리에 저장됨.
 - iOS는 앱 실행 시 특정 메모리 영역을 할당하여 사용하게 함.
 - 변수, 상수, 코드도 메모리에 저장됨.

 메모리 효율적으로 사용하기
 - 정리되지 않은 데이터는 메모리 낭비를 초래하고 성능 저하 가능.
 - 데이터를 효율적으로 저장하려면 자료구조(Array, Set 등) 활용 필요.
 */
