/**
 타입 캐스팅
 - class에서 상속 관계를 이용한 타입 캐스팅이 주로 사용됨
 - struct와 enum에서는 상속을 통한 타입 캐스팅을 지원하지 않음
 - is: 타입을 확인하는 키워드. 타입이 특정클래스 혹은 하위클래스인 경우 true, 아닐경우 false를 반환
 - as: 슈퍼클래스로 변환하는 타입 캐스팅 방법 (업캐스팅)
 - as?: 서브클래스로 변환하는 타입 캐스팅 방법 (Optional로 다운 캐스팅)
 - as!: 서브클래스로 변환하는 타입 캐스팅 방법 (실패하면 크래시 발생)
 - 인스턴스 이름 뒤에 키워드(is, as, as?, as!) 특정 타입 으로 사용
 */
