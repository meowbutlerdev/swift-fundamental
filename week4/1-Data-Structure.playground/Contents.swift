/**
 Array: 배열
 - 메모리 공간에 연속적으로 데이터를 저장하는 자료구조
 - 인덱스를 통한 접근이 빠름
 - 배열 중간에 데이터를 추가하거나 삭제할 때 느림

 Linked List
 - 각 데이터가 다음 데이터의 주소를 가리켜 순서를 보장하는 자료 구조
 - 중간에 값을 추가하거나 삭제할 때 빠름
 - 인덱스를 통한 접근이 느림
 - 다음 데이터의 주소값을 저장해야 하므로 추가적인 메모리 필요

 Stack
 - 후입선출(Last In First Out) 방식의 자료구조
 - 먼저 들어간 데이터가 나중에 나옴
 - push를 사용해 데이터를 추가하고 pop을 사용해 데이터 제거

 Queue
 - 선입선출(First In First Out) 방식의 자료구조
 - 먼저 들어간 데이터가 먼저 나옴
 - enqueue를 사용해 데이터를 추가하고, dequeue를 사용해서 데이터 제거
 */
