/**
 고차함수
 - 다른 함수를 인자로 받거나, 함수를 반환하는 함수
 - map
   - 배열의 각 요소를 반환하여 새로운 배열 생성
   - 각 요소를 변형하는 작업을 반복하며, 변형된 값을 새로운 배열로 반환함
 - filter
   - 배열에서 주어진 조건에 맞는 요소들만 걸러내어 새로운 배열로 반환
 - reduce
   - 배열의 모든 요소를 하나로 합쳐 하나의 값을 반환
   - 초기값이 필요함
 - compactMap
   - 옵셔널 값을 언래핑하고 nil을 제거한 배열을 반환
 - flatMap
   - 중첩된 컬렉션을 평평하게 만드는 데 사용(2차원 배열을 1차원 배열로 만듬)
 */
